package com.ayungi.cms.entity.enums;

/**
 * Статусы банковской карты
 */
public enum CardStatus {
    /**
     * Карта активна и может использоваться
     */
    ACTIVE,

    /**
     * Карта заблокирована (по запросу пользователя или администратора)
     */
    BLOCKED,

    /**
     * Срок действия карты истек
     */
    EXPIRED
}
